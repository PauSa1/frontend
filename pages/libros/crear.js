const BookCreate= () => {
     const router = useRouter();
     const [bookTitle, setBookTitle] = useState('');
     const [errors, setErrors] = useState([]);
     const [submitting, setSubmitting] = useState(false);

     async function handleSubmit(e) {
          e.preventDefault()
          setSubmitting(true)
         const res = await fetch(`${process.env.NEXT_PUBLIC_BACKEND_URL}/api/books`, {
          method: 'POST',
          headers: {
               accept: 'application/json',
               'content-type': 'application/json',
          },
          body: JSON.stringify({
               title: bookTitle
          })
         })////////Validacion de formulario en caso de enviar un libro en blanco////////////
         if(res.ok){
          //funciono
          setErrors([])
          setBookTitle('')
          return router.push('/libros') 
         }
          //Fallo
          const data = await res.json()
          setErrors(data.errors)
          setSubmitting(false)
     }
     return (
          <>
               <h1>BookCreate</h1>
               <form onSubmit={handleSubmit}>
                    <input
                     onChange={(e) => setBookTitle(e.target.value)}
                     value={bookTitle}
                     disabled={submitting}//si se envia se deshabilita el input
                    type="text"
                    />
                         <button 
                         disabled={submitting}//si se envia se deshabilita el botÃ²n
                         >{submitting ? 'Enviando' : 'Enviar'}
                         </button>
                         
                         {errors.title && (//Para mostrar el error del campo vacio
                              <span style={{color: 'red', display:'block'
                         }}>{errors.title}</span>
                         )}
               </form>
               <br/>
               <Link href="/libros">Book List</Link>
               
          </>
     );
};

export default BookCreate;